// Write a code for the output of the grades. if the mark is more than equals to 80, the grade is A. If the mark is more than equals to 70, the grade is B. If the mark is more than equals to 60, the grade is C. if the mark is more than equals to 50, the grade is D. Otherwise print the result as F.

var number = 45;

if (number >= 80) {
  console.log("A");
} else if (number >= 70) {
  console.log("B");
} else if (number >= 60) {
  console.log("C");
} else if (number >= 50) {
  console.log("D");
} else {
  console.log("F");
}
